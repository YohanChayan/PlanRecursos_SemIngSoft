use Illuminate\Http\Request;

class NotificationController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $notifications = Notification::where('id_receptor', auth()->user()->id)->get();
        
        return view('notifications.notificationsIndex', compact('notifications'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $users = User::where('role', auth()->user()->role)->get();
        $SUP = User::where('id', auth()->user()->id_sup)->first();

        return view('notifications.notificationsCreate',compact('users', 'SUP'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Notification::create($request->all());
        return redirect()->route('notifications.index');
    }

